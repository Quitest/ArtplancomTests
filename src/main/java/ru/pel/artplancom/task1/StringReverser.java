package ru.pel.artplancom.task1;

/**
 * Интерфейс классов "разворачивотелей". Содержит единственный метод разворота строки.
 */
public interface StringReverser {
    /**
     * Метод разворачивает строку в обратном порядке
     * @param str исходная строка
     * @return развернутая строка
     */
    String reverse(String str);
}
